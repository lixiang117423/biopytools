#!/bin/bash

# ====================================================================
# 测试任务脚本
# 功能：此任务会运行大约1分钟，然后正常退出。
#       它会打印开始和结束时间，方便您验证。
# ====================================================================

# 1. 打印任务开始的信息
#    这些信息会输出到您的任务日志文件中。
echo "============================================="
echo "这是一个测试任务，它将运行60秒。"
echo "任务开始时间: $(date)"
echo "运行节点: $(hostname)"
echo "============================================="
echo ""

# 2. 核心工作：使用 sleep 命令暂停60秒
echo "任务正在运行中，请等待..."
sleep 60
echo "60秒已过。"
echo ""

# 3. 打印任务结束的信息
echo "============================================="
echo "测试任务已成功完成！"
echo "任务结束时间: $(date)"
echo "============================================="
